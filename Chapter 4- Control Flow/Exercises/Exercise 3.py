#Assigning variable
alien_color = 'green'

#Start with if function
if alien_color == 'green':
    print ('You earned 5 points!')

#Elif function in case alien color is not green
elif alien_color == 'yellow':
    print ('You earned 10 points!')

#Else function used incase alien color is not green and yellow
else:
    print ('You earned 15 points!')

